@Pipe({standalone: true, name: 'ingredientGMapToAllergens'})
export class IngredientGMapToAllergensPipe implements PipeTransform {
  transform(ingredientGroups: IngredientGroup[]): any {
    return uniqueArray(ingredientGroups.flatMap((iG) =>
      iG.ingredients.flatMap((i) =>
        i.material.allergens
      )
    ), ["id"]);
  }
}

@Pipe({standalone: true, name: 'ingredientGMapToMaterials'})
export class IngredientGMapToMaterialsPipe implements PipeTransform {
  transform(ingredientGroups: IngredientGroup[]): any {
    return uniqueArray(ingredientGroups.flatMap((iG) =>
      iG.ingredients.flatMap((i) =>
        i.material
      )
    ), ["id"]);
  }
}

const uniqueArray = (objects: any[], uniqueBy: string[], keepFirst = true) => {
  return Array.from(
    objects.reduce((map, e) => {
      let key = uniqueBy.map(key => [e[key], typeof e[key]]).flat().join('-')
      if (keepFirst && map.has(key)) return map
      return map.set(key, e)
    }, new Map()).values()
  )
}
